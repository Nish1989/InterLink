package com.interlink.vo;

public class UserInfoVo {
	
	
	private String userId;
	
	private String password;
	
	private String emailId;
	
	private String role;
	
	private String name;
	
	private String collegeName;
	
	private String collegeAddr;
	
	private String city;

	private String createdDate;
	
	private int noOfProfile;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCollegeName() {
		return collegeName;
	}

	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}

	public String getCollegeAddr() {
		return collegeAddr;
	}

	public void setCollegeAddr(String collegeAddr) {
		this.collegeAddr = collegeAddr;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}

	public int getNoOfProfile() {
		return noOfProfile;
	}

	public void setNoOfProfile(int noOfProfile) {
		this.noOfProfile = noOfProfile;
	}



	

}
