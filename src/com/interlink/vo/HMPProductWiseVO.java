public class HMPProductWiseVO {
	
	
	
	private String productCatg;
	private String subProductCatg;
	private String skuitem;
	private String mrp;
	private String quantity;
	private String amt;

	
	
	public String getProductCatg() {
		return productCatg;
	}
	public void setProductCatg(String productCatg) {
		this.productCatg = productCatg;
	}
	public String getSubProductCatg() {
		return subProductCatg;
	}
	public void setSubProductCatg(String subProductCatg) {
		this.subProductCatg = subProductCatg;
	}
	public String getSkuitem() {
		return skuitem;
	}
	public void setSkuitem(String skuitem) {
		this.skuitem = skuitem;
	}
	public String getMrp() {
		return mrp;
	}
	public void setMrp(String mrp) {
		this.mrp = mrp;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getAmt() {
		return amt;
	}
	public void setAmt(String amt) {
		this.amt = amt;
	}
	
	
	

}
